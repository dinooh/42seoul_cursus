#include "ft_printf.h"

void	ft_left_or_zero(const char **ppf)
{
	while (**ppf == '-' || **ppf == '0')
	{
		if (**ppf == '-')
			l.is_left = 1;
		if (**ppf == '0' && l.is_left == 0)
			l.is_zero = 1;
		*ppf += 1;
	}
}