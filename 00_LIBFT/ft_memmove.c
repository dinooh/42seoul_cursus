#include "libft.h"

void				*ft_memmove(void *dest, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	if (!dest && !src)
		return (0);
	d = dest;
	s = (unsigned char *)src;
	if (dest <= src)
	{
		while (len--)
			*(d++) = *(s++);
	}
	else
	{
		d += len;
		s += len;
		while (len--)
			*(--d) = *(--s);
	}
	return (dest);
}
